import { pig } from './help'

// test pig()
for (const [ arr, assert ] of [
  [['2','-','3','1','4','3','5','-'], 14],
  [['2','-','3','1','4','3','5','-','6'], 20],
  [['1','4','5','-','6','-'], 15],
  [['1','4','5','-','6','-','2'], 17],
  [['6','6','1','5','-','6','2','3','1'], 5],
  [['6','6','1','5','-','6','2','3','1','3'], 8],
  [['3','4','-','1','2','-','3','1'], 9],
  [['3','4','-','1','2','-','3','1','3'], 12]
]) {
  const res = pig(arr)
  console.assert(res === assert, `pig(${arr}) = ${res}; ${res} != ${assert}`)
}
